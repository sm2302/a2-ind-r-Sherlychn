# Q1 ---------------------------------------------------------------------------
pareto_pdf <- function(x, alpha, beta) {
  fx <- ((alpha)*(beta^alpha)) / ((x)^(alpha + 1))
  return(fx)
}

# Q2 ---------------------------------------------------------------------------
pareto_dev <- function(alpha, beta, x) {
  gx <- 2*(log(alpha) + alpha*log(beta))*(sum(alpha*log(x)+log(x)))
  return(gx)
}

